from dataclasses import asdict, dataclass
from typing import Dict

import numpy
import torch
from ase import Atoms
from torch import Tensor, tensor

from pfp.nn.estimator_base import EstimatorSystem


def create_dummy_input() -> EstimatorSystem:
    atoms = Atoms(
        "H2",
        positions=[(0, 0, 0), (0, 0, 1)],
        pbc=(False, False, False),
        cell=[2, 2, 2],
    )
    species = numpy.array(atoms.get_chemical_symbols(), dtype="<U3")
    atomic_numbers = atoms.get_atomic_numbers().astype("uint8")
    cell = atoms.get_cell(complete=True).array
    coordinates = atoms.get_positions()
    pbc = atoms.get_pbc()
    return EstimatorSystem(
        properties=[],
        species=species,
        coordinates=coordinates,
        atomic_numbers=atomic_numbers,
        cell=cell,
        pbc=pbc,
    )


@dataclass
class Input:
    vecs: Tensor
    coordinates: Tensor
    atomic_numbers: Tensor
    left_indices: Tensor
    right_indices: Tensor
    shift: Tensor
    cell: Tensor
    num_graphs: Tensor
    batch: Tensor
    batch_edge: Tensor
    x_add: Tensor
    calc_mode_type: Tensor

    def get_codegen_input(self, coord_dtype: torch.dtype) -> Dict[str, Tensor]:
        inputs = asdict(self)
        inputs["atom_index1"] = self.left_indices
        inputs["atom_index2"] = self.right_indices
        inputs["coordinates"] = inputs["coordinates"].to(coord_dtype)
        inputs["cell"] = inputs["cell"].to(coord_dtype)
        del inputs["vecs"]
        del inputs["left_indices"]
        del inputs["right_indices"]
        return inputs


